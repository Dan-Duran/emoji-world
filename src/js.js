function copyEmoji(char) {
navigator.clipboard.writeText(char).then(() => {
    feedback.style.display = 'block';
    setTimeout(() => {
        feedback.style.display = 'none';
    }, 1500);
}).catch(err => {
    console.error('Failed to copy:', err);
    feedback.textContent = 'Failed to copy ğŸ˜•';
    feedback.style.display = 'block';
    setTimeout(() => {
        feedback.style.display = 'none';
        feedback.textContent = 'Copied! ğŸ“‹';
    }, 1500);
});
}

function renderSearchResults(results) {
if (results.length === 0) {
    container.innerHTML = `
        <div class="no-results">
            No emojis found matching "${search.value}" ğŸ”
        </div>
    `;
    return;
}

const emojiGrid = document.createElement('div');
emojiGrid.className = 'emoji-grid';

results.forEach(({ char, name, category }) => {
    const card = document.createElement('div');
    card.className = 'emoji-card';
    card.innerHTML = `
        <div class="emoji">${char}</div>
        <div class="name">${name}</div>
        <div class="category-tag">${category}</div>
    `;
    card.onclick = () => copyEmoji(char);
    emojiGrid.appendChild(card);
});

container.innerHTML = '';
container.appendChild(emojiGrid);
}

function getCategoryId(category) {
return category.toLowerCase().split(' & ')[0].split(' ')[0];
}

